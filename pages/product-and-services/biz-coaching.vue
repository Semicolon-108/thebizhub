<script setup lang="ts">
import patternDivider from "./pattern.vue";
import Gallery from "../about-us/gallery.vue";
const axios = useNuxtApp().$axios;
const urlImage = useNuxtApp().$imageURL;
import { useI18n } from "vue-i18n";
const { locale }: any = useI18n();
const isLang = ref<any>();
const ShowGallery = ref<any>(false);
const info = ref<any>([]);
const area = ref<any>("Business Coaching");
const isImage = ref<any>("")
const fetch = async () => {
  await axios
    .post(`get-achievement-api?lang=${isLang.value}&area=${area.value}`)
    .then((res: any) => {
      if (res) {
        info.value = res.data.info;
      }
    });
};
watch(
  () => locale.value,
  (value) => {
    isLang.value = value;
    fetch();
  },
  { immediate: true, deep: true }
);


const isFindIndex = (obj: any, image: any) => {
  const find = image.find((f: any) => f == obj)
  isImage.value = find
  ShowGallery.value = true
}
</script>

